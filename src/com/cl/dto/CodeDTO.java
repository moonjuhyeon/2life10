package com.cl.dto;

public class CodeDTO {
	private String codeId;
	private String codeName;
	private String grpCodeId;
	private String grpCodeName;
	private String regMemberNo;
	private String regDt;
	private String chgMemberNo;
	private String chgDt;
	
	public String getCodeId() {
		return codeId;
	}
	public void setCodeId(String codeId) {
		this.codeId = codeId;
	}
	public String getCodeName() {
		return codeName;
	}
	public void setCodeName(String codeName) {
		this.codeName = codeName;
	}
	public String getGrpCodeId() {
		return grpCodeId;
	}
	public void setGrpCodeId(String grpCodeId) {
		this.grpCodeId = grpCodeId;
	}
	public String getGrpCodeName() {
		return grpCodeName;
	}
	public void setGrpCodeName(String grpCodeName) {
		this.grpCodeName = grpCodeName;
	}
	public String getRegMemberNo() {
		return regMemberNo;
	}
	public void setRegMemberNo(String regMemberNo) {
		this.regMemberNo = regMemberNo;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgMemberNo() {
		return chgMemberNo;
	}
	public void setChgMemberNo(String chgMemberNo) {
		this.chgMemberNo = chgMemberNo;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	
}
