package com.cl.dto;

public class AccDTO {
	private String memberNo;
	private String paymentDay;
	private String paymentCount;
	private String paymentMoney;
	private String lastPaymentCount;
	private String paymentRoute;
	private String realPaymentDay;
	private String discountMoney;
	private String etc;
	public String getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(String memberNo) {
		this.memberNo = memberNo;
	}
	public String getPaymentDay() {
		return paymentDay;
	}
	public void setPaymentDay(String paymentDay) {
		this.paymentDay = paymentDay;
	}
	public String getPaymentCount() {
		return paymentCount;
	}
	public void setPaymentCount(String paymentCount) {
		this.paymentCount = paymentCount;
	}
	public String getPaymentMoney() {
		return paymentMoney;
	}
	public void setPaymentMoney(String paymentMoney) {
		this.paymentMoney = paymentMoney;
	}
	public String getLastPaymentCount() {
		return lastPaymentCount;
	}
	public void setLastPaymentCount(String lastPaymentCount) {
		this.lastPaymentCount = lastPaymentCount;
	}
	public String getPaymentRoute() {
		return paymentRoute;
	}
	public void setPaymentRoute(String paymentRoute) {
		this.paymentRoute = paymentRoute;
	}
	public String getRealPaymentDay() {
		return realPaymentDay;
	}
	public void setRealPaymentDay(String realPaymentDay) {
		this.realPaymentDay = realPaymentDay;
	}
	public String getDiscountMoney() {
		return discountMoney;
	}
	public void setDiscountMoney(String discountMoney) {
		this.discountMoney = discountMoney;
	}
	public String getEtc() {
		return etc;
	}
	public void setEtc(String etc) {
		this.etc = etc;
	}
}
